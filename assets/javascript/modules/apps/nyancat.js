  function openNyanCat() {
    var r = window.Site.makeFloatWin(
      '<i class="fa-solid fa-cat"></i> nyancat', 620, 320, 'nyancat-window'
    );
    if (!r) return;
    var canvas = document.createElement('canvas');
    canvas.style.cssText = 'display:block;width:100%;height:100%;';
    r.body.style.cssText = 'padding:0;background:#00005f;overflow:hidden;';
    r.body.appendChild(canvas);

    var stopped = false;
    var frameIdx = 0;
    var RAINBOW = ',,>>&&&+++###==;;;,,';
    var MIN_ROW = 20, MAX_ROW = 44, MIN_COL = 12, MAX_COL = 52;

    function render() {
      if (stopped) return;
      var W = canvas.offsetWidth || 620;
      var H = canvas.offsetHeight || 200;
      if (canvas.width !== W) canvas.width = W;
      if (canvas.height !== H) canvas.height = H;
      var ctx = canvas.getContext('2d');
      var rows = MAX_ROW - MIN_ROW;
      var cols = MAX_COL - MIN_COL;
      var cw = W / cols;
      var ch = H / rows;
      var frame = FRAMES[frameIdx];
      for (var y = MIN_ROW; y < MAX_ROW; y++) {
        for (var x = MIN_COL; x < MAX_COL; x++) {
          var c;
          if (y >= 23 && y < 43 && x < 23) {
            var mod_x = ((-(x) + 2) % 16 + 16) % 16;
            mod_x = (mod_x / 8) | 0;
            if (((frameIdx >> 1) & 1)) mod_x = 1 - mod_x;
            c = RAINBOW[mod_x + (y - 23)] || ',';
          } else if (x < 0 || y < 0 || y >= 64 || x >= 64) {
            c = ',';
          } else {
            var row = frame[y];
            c = (row && row[x]) || ',';
          }
          ctx.fillStyle = COLORS[c] || '#00005f';
          ctx.fillRect(
            Math.floor((x - MIN_COL) * cw),
            Math.floor((y - MIN_ROW) * ch),
            Math.ceil(cw) + 1,
            Math.ceil(ch) + 1
          );
        }
      }
      frameIdx = (frameIdx + 1) % 12;
      setTimeout(function () {
        if (!stopped) requestAnimationFrame(render);
      }, 150);
    }

    requestAnimationFrame(render);

    var observer = new MutationObserver(function () {
      if (!document.body.contains(r.win)) {
        stopped = true;
        observer.disconnect();
      }
    });
    observer.observe(document.body, { childList: true, subtree: true });
  }

  window.Site.openNyanCat = openNyanCat;
